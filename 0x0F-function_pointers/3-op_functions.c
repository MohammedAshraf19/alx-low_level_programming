#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"

/**
* op_add - add to integer.
* @a: integer.
* @b: integer.
* Return: integer.
*/
int op_add(int a, int b)
{
		return (a + b);
}

/**
* op_sub - sub tow integer.
* @a: integer.
* @b: integer.
* Return: integer.
*/
int op_sub(int a, int b)
{
		return (a - b);
}

/**
* op_mul - multi tow integer.
* @a: integer.
* @b: integer.
* Return: integer.
*/
int op_mul(int a, int b)
{
		return (a * b);
}

/**
* op_div - divide tow integer.
* @a: integer.
* @b: integer.
* Return: integer.
*/
int op_div(int a, int b)
{
		return (a / b);
}

/**
* op_mod - mod tow integer.
* @a: integer.
* @b: integer.
* Return: integer.
*/
int op_mod(int a, int b)
{
		return (a % b);
}

